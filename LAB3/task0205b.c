#include <stdio.h>

int main() {
    double r, a, b, z, c;

    printf("Введите радиус торта r: ");
    scanf("%lf", &r);
    printf("Введите стороны коробки a и b: ");
    scanf("%lf %lf", &a, &b);
    printf("Введите высоту торта z: ");
    scanf("%lf", &z);
    printf("Введите высоту коробки c: ");
    scanf("%lf", &c);

    if (2 * r <= a && 2 * r <= b) {
        printf("Торт радиусом %.2lf уместится в коробку по площади основания.\n", r);
    } else {
        printf("Торт радиусом %.2lf НЕ уместится в коробку по площади основания.\n", r);
    }

    if (2 * r <= a && 2 * r <= b && z <= c) {
        printf("Торт радиусом %.2lf и высотой %.2lf уместится в коробку с высотой %.2lf.\n", r, z, c);
    } else {
        printf("Торт радиусом %.2lf и высотой %.2lf НЕ уместится в коробку с высотой %.2lf.\n", r, z, c);
    }

    return 0;
}


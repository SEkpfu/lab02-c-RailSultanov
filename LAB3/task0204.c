#include <stdio.h>

int main() {
    int dM, dW;
    printf("Введите дату: \n");
    printf("День месяца: ");
    scanf("%d", &dM);
    printf("Номер дня в неделе (1-Понедельник, ..., 5-Пятница, ..., 7-Воскресенье): ");
    scanf("%d", &dW);

    if (dM == 13) {
        if (dW == 5) {
            printf("Неудачный день (пятница 13-е)\n");
        } else {
            if (dW == 2) {
                printf("Неудачный день (вторник 13-е)\n");
            }
        }
    }
}
